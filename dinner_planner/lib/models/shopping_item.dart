class ShoppingItem {
  final String name;
  double totalQuantity;
  final String unit;
  double totalCalories;
  double totalProtein;
  double totalCarbs;
  double totalFat;

  ShoppingItem({
    required this.name,
    required this.totalQuantity,
    required this.unit,
    required this.totalCalories,
    required this.totalProtein,
    required this.totalCarbs,
    required this.totalFat,
  });
}
